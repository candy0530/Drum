void PlayingScene::GameLogic(float dt){

    if(CocosDenshion::SimpleAudioEngine::sharedEngine()->isBackgroundMusicPlaying()){

        log("Logging Test: %d, mu1_size: %d", count, sizeof(mu1));

        if(count>=sizeof(mu1)/4){
            while(CocosDenshion::SimpleAudioEngine::sharedEngine()->isBackgroundMusicPlaying()){
                log("waiting music end");
            }
            log("music end");
            this->unschedule(schedule_selector(PlayingScene::GameLogic));
            showEnd();
            end_flag=1;
            return;
        }
        if(mu1[count]){
            create_obj1(mu1[count]);
        }
        if(mu2[count]){
            create_obj2(mu2[count]);
        }
        if(mu3[count]){
            create_obj3(mu3[count]);
        }
        if(mu4[count]){
            create_obj4(mu4[count]);
        }
        if(mu5[count]){
            create_obj5(mu5[count]);
        }
        count++;
    }else{
        if(end_flag==0 && data_pause==0){
            log("music end");
            showEnd();
            end_flag = 1;
        }
    }
}

