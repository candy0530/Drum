void PlayingScene::Ai(int click){

    float random = CCRANDOM_0_1();
    int ai_click ;

    if(random<=level){
        ai_click = 1;
        hit_e++;
    }else{
        ai_click = 0;
    }

    //    log("Me:%d , Enemy:%d",click,ai_click);

    if(click>ai_click){
        Rope(1);
    }else if(click<ai_click){
        Rope(0);
    }

}
void PlayingScene::Rope(int left){
    float bounce = 10;
    float Max = -325;
    float Min = 325;

    if(left==1){
        if(rope_x>=Max){
            rope_x = rope_x-10;
            rope->setPosition(Vec2(rope_x,-225));
        }
    }else{
        if(rope_x<=Min){
            rope_x = rope_x+10;
            rope->setPosition(Vec2(rope_x,-225));
        }
    }
}

