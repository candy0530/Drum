void PlayingScene::Run(){

    this->unschedule(schedule_selector(PlayingScene::GameLogic));
    count = 16*current_beats;
    float duration_time = first_beats_time[current_beats+1]-first_beats_time[current_beats];
    float beats_time = duration_time/16.000;
    this->schedule(schedule_selector(PlayingScene::GameLogic),beats_time);
    log("Logging Test: Start to Recursive");
    if(current_beats>24){
        log("Logging Test: finished");
    }else{
        log("Logging Test: current_beats:%d", current_beats);
        auto action = CallFunc::create(this, callfunc_selector(PlayingScene::Run));
        this->runAction(Sequence::create(DelayTime::create(duration_time), action, NULL));
        current_beats++;
        log("Logging Test: current_beats end:%d", current_beats);
    }
}

